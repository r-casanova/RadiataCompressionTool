def generate_name_overrides() -> dict[int, str]:
    """Build complete index→label map with category prefixes."""
    overrides = {}

    for idx, name in SYSTEM_LABELS.items():
        overrides[idx] = f"system_{name}"
    for idx, name in AREA_LABELS.items():
        overrides[idx] = f"area_{name}"
    for idx, name in CHARACTER_LABELS.items():
        overrides[idx] = f"char_{name}"
    for idx, name in MONSTER_LABELS.items():
        overrides[idx] = f"monster_{name}"
    for idx, name in EQUIP_LABELS.items():
        overrides[idx] = f"equip_{name}"
    for idx, name in PROP_LABELS.items():
        overrides[idx] = f"prop_{name}"
    for idx, name in ANIMATION_LABELS.items():
        overrides[idx] = f"anim_{name}"

    for index in range(1511, 1688):
        if index not in overrides:
            overrides[index] = f"monster_{index - 1510:04d}"
    for index in range(1688, 1939):
        if index not in overrides:
            overrides[index] = f"prop_{index - 1687:04d}"
    for index in range(1939, 2126):
        if index not in overrides:
            overrides[index] = f"equip_{index - 1938:04d}"
    for index in range(2126, 2426):
        overrides[index] = f"vfx_{index - 2125:04d}"

    return overrides
